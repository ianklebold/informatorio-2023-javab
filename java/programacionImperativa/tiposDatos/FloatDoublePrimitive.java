package programacionImperativa.tiposDatos;

public class FloatDoublePrimitive {
    public static void main(String[] args) {
        
        //float tiene 32 bits
        float miMinimoValorDeFloat = Float.MIN_VALUE;
        float miMaximoValorDeFloat = 3.40f;
        
        System.out.println("Float valor minimo : " + miMinimoValorDeFloat);
        System.out.println("Float valor maximo : " + miMaximoValorDeFloat);

        //double tiene 64 bits

        double miMinimoValorDeDouble = Double.MIN_VALUE;
        double miMaximoValorDeDouble = Double.MAX_VALUE;

        System.out.println("Double valor minimo : " + miMinimoValorDeDouble);
        System.out.println("Double valor maximo : " + miMaximoValorDeDouble);

    }
}
